    q = questions[current_q]
    try:
        h, g = q["scores"][ans]
    except KeyError:
        messagebox.showerror("Error", f"Invalid answer '{ans}' selected.")
        app.destroy()
        return
    total_human += h
    total_gorilla += g
    if "befriend" in q["text"].lower() and ans == "yes":
        befriend = True
    if "tame or bond" in q["text"].lower() and ans == "yes":
        befriend = True

    current_q += 1
    if current_q < len(questions):
        show_question()
    else:
        end_game()

